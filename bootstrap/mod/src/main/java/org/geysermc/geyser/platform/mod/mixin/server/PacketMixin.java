package org.geysermc.geyser.platform.mod.mixin.server;

import net.minecraft.network.protocol.common.ServerCommonPacketListener;
import net.minecraft.network.protocol.common.ServerboundCustomPayloadPacket;
import net.minecraft.network.protocol.common.custom.CustomPacketPayload;
import org.geysermc.geyser.GeyserImpl;
import org.spongepowered.asm.mixin.Final;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Shadow;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(ServerboundCustomPayloadPacket.class)
public abstract class PacketMixin {

    @Shadow
    @Final
    private CustomPacketPayload payload;

    @Inject(method = "handle(Lnet/minecraft/network/protocol/common/ServerCommonPacketListener;)V", at = @At(value = "HEAD"))
    private void handle(ServerCommonPacketListener serverCommonPacketListener, CallbackInfo ci) {
        GeyserImpl.getInstance().getLogger().warning("Attempting to handle serverbound custom payload packet with resource location %s"
            .formatted(this.payload.type().id() + " " + this.payload.type().getClass().getSimpleName()));
    }
}
